#include "push_swap_game.h"

int	ctrl_sorted(t_stack *a, int size)
{
	t_stack	*tmp;

	tmp = a;
	if (size == len_stack(&a))
	{
		while (tmp && tmp->next)
		{
			if (tmp->num > tmp->next->num)
				return (0);
			tmp = tmp->next;
		}
		return (1);
	}
	return (0);
}
