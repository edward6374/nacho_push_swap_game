#include <stdio.h>

void print(int *n)
{
	int i = 0;
	while (i <= 4)

	{
		printf("%d\n", n[i]);
		i++;
	}
}

int main(void)
{
	int a[] = {1, 2, 3, 4, 5};
	int b[5];
	int i = 0;

	while (i <= 4)
	{
		b[i] = a[i];
		i++;
	}

	print(a);
	printf("---\n");
	print(b);

	return (0);
}
